       <h2>Fakultas Teknik Universitas Hasanuddin</h2>
         <h3>Sejarah</h3>
         <p id="footer">Fakultas Teknik Universitas Hasanuddin berdiri sejak 7 September 1960, yang terdiri atas tiga jurusan, yakni Teknik Sipil, Teknik Mesin dan Teknik Perkapalan. Pengembangan fakultas melalui pembukaan jurusan baru tetap berjalan sampai bulan September 1963, saat terbentuk Jurusan Teknik Arsitektur dan Teknik Elektro. Pembentukan Jurusan Teknik Geologi pada bulan September 1982 menutup rangkaian pengembangan jurusan pada Fakultas Teknik.<br><br>
         Saat ini Fakultas Teknik terdiri dari 13 Departemen Sarjana, 6 Program Studi Magister dan 5 Program Studi Doktor.Fakultas Teknik Universitas Hasanuddin sekarang berlokasi di Kabupaten Gowa Sulawesi Selatan lebih tepatnya di Jl. Poros Malino, Borongloe,Bontomarannu, Kabupaten Gowa</p> <br><br>

